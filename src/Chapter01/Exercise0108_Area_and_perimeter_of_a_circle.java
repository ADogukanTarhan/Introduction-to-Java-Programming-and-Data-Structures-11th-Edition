/**
 * (Area and perimeter of a circle) Write a program that displays the area and perimeter
 * of a circle that has a radius of 6.5 using the following formula:
 * PI = 3.14159
 * perimeter = 2 * radius * pi
 * area = radius * radius * pi
 *
 * Created by Sven on 8/16/2018.
 */
package Chapter01;

public class Exercise0108_Area_and_perimeter_of_a_circle {
    public static void main(String[] args) {
        System.out.println("The area of a circle that has a radius of 6.5 is " + 6.5 * 6.5 * 3.14159);
        System.out.println("The perimeter of a circle that has a radius of 6.5 is " + 2 * 6.5 * 3.14159);
    }
}
