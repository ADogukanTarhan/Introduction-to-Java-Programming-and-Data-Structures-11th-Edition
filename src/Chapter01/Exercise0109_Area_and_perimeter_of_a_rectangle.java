/**
 * (Area and perimeter of a rectangle) Write a program that displays the area and perimeter
 * of a rectangle with a width of 5.3 and height of 8.6 using the following formula:
 * area = width * height
 * perimeter = 2 * (width + height)
 *
 * Created by Sven on 8/16/2018.
 */
package Chapter01;

public class Exercise0109_Area_and_perimeter_of_a_rectangle {
    public static void main(String[] args) {
        System.out.println("The area of a rectangle with the width of 5.3 and height of 8.6 is " + 5.3 * 8.6);
        System.out.println("The perimeter of a rectangle with the width of 5.3 and height of 8.6 is " + 2 * (5.3 + 8.6));
    }
}
